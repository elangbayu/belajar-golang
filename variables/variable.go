package main

import "fmt"

func main() {
  var name string

  name = "Elang Segara"
  fmt.Println(name)

  name = "Elang Bayu"
  fmt.Println(name)

  var surname = "Elang Bayu Segara"
  fmt.Println(surname)

  var age = 24
  fmt.Println(age)

  country := "Indonesia"
  fmt.Println(country)

  var (
    firstName = "Elang"
    lastName = "Segara"
  )

  fmt.Println(firstName)
  fmt.Println(lastName)
}

